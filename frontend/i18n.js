const subscribers = new Set();

export function t(key) {
  return (dict[current] && dict[current][key]) || (dict.en && dict.en[key]) || key;
}
export function getLang() { return current; }
export function setLang(code) {
  if (!dict[code]) return;
  current = code;
  localStorage.setItem('lang', code);
  document.documentElement.lang = code === 'zh' ? 'zh-CN' : 'en';
  subscribers.forEach(fn => { try { fn(code); } catch (_) {} });
}
export function onLangChange(fn) { subscribers.add(fn); return () => subscribers.delete(fn); }

